#Assignment Operators

'''
=   assignmrnt
+=  addition
-=  subtraction
*=  multiplication
/=  division
%=  modulus
**= Exponentiation
//= Floor division
'''

a=125
a+=5 #a=a+5
print(a)
a-=5 #a=a-5
print(a)
a*=5 #a=a*5
print(a)
a/=5 #a=a/5
print(a)
a%=5 #a=a%5
print(a)
a**=5 #a=a**5
print(a)
a//=5 #a=a//5
print(a)
