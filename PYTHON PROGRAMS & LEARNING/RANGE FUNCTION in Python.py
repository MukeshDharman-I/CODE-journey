# RANGE FUNCTION in Python
'''
range(5) => 0,1,2,3,4,
range(2,5)  => 2,3,4
'''
a=list(range(5))
print(a)

print("---------------------")

print(list(range(2,5)))                   # can execute without a variable

print("---------------------")
'''
print even numbers from 0 to 20 using range
'''
print(list(range(0,21,2)))

print("---------------------")
'''
print odd numbers from 0 to 20 using range
'''
print(list(range(1,21,2)))